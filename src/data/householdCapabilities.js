export function getHouseholdCapabilitySummary(completed = {}) {
  const completedMap = completed && typeof completed === 'object' ? completed : {}
  const systems = HOUSEHOLD_CAPABILITIES.map((capability) => {
    const tasks = HOUSEHOLD_CAPABILITY_TASKS.filter((task) => task.system === capability.id)
    const done = tasks.filter((task) => Boolean(completedMap[task.id])).length
    return { ...capability, done, total: tasks.length, percent: tasks.length ? Math.round((done / tasks.length) * 100) : 0 }
  })
  const done = systems.reduce((sum, item) => sum + item.done, 0)
  const total = HOUSEHOLD_CAPABILITY_TASKS.length
  return { done, total, percent: total ? Math.round((done / total) * 100) : 0, systems, weakest: [...systems].sort((a,b)=>a.percent-b.percent)[0], nextTask:HOUSEHOLD_CAPABILITY_TASKS.find((task)=>!completedMap[task.id]) || null }
}
