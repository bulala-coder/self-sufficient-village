export function getTasks(routeType = 'balcony_beginner') {
  const tasks = baseTasks.map((task, i) => ({
    id: `base-${i + 1}`,
    day: i + 1,
    ...task
  }))

  const branches = branchTasks[routeType] || []
  branches.forEach((task, idx) => {
    tasks[10 + idx] = {
      id: `${routeType}-${idx + 1}`,
      day: 11 + idx,
      ...task
    }
  })

  return tasks
}
